                <p>
                    The pilgrimage occurs from the 8th to 12th (or in some cases 13th of Dhu al-Hijjah, the last month of the Islamic calendar. Because the Islamic calendar is lunar and the Islamic year is about eleven days shorter than the Gregorian year, the Gregorian date of Hajj changes from year to year. Ihram is the name given to the special spiritual state in which pilgrims wear two white sheets of seamless cloth and abstain from certain actions.</p>
                    <p>
                    The Hajj is associated with the life of Islamic prophet Muhammad from the 7th century, but the ritual of pilgrimage to Mecca is considered by Muslims to stretch back thousands of years to the time of Abraham. During Hajj, pilgrims join processions of hundreds of thousands of people, who simultaneously converge on Mecca for the week of the Hajj, and perform a series of rituals: each person walks counter-clockwise seven times around the Ka'aba (the cube-shaped building and the direction of prayer for the Muslims), runs back and forth between the hills of Al-Safa and Al-Marwah, drinks from the Zamzam Well, goes to the plains of Mount Arafat to stand in vigil, spends a night in the plain of Muzdalifa, and performs symbolic stoning of the devil by throwing stones at three pillars. The pilgrims then shave their heads, perform a ritual of animal sacrifice, and celebrate the three-day global festival of Eid al-Adha.</p>
                    <p>
                    Pilgrims can also go to Mecca to perform the rituals at other times of the year. This is sometimes called the "lesser pilgrimage", or Umrah. However, even if they choose to perform the Umrah, they are still obligated to perform the Hajj at some other point in their lifetime if they have the means to do so, because Umrah is not a substitute for Hajj. </p>

