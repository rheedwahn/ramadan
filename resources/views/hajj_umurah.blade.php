                <h3> HAJJ </h3>
                <p> The Hajj is an annual Islamic pilgrimage to Mecca, the most holy city of the Muslims, and a mandatory religious duty for Muslims that must be carried out at least once in their lifetime by all adult Muslims who are physically and financially capable of undertaking the journey, and can support their family during their absence. It is one of the five pillars of Islam, alongside Shahadah, Salat, Zakat, and Sawm. The Hajj is the largest annual gathering of people in the world. The state of being physically and financially capable of performing the Hajj is called istita'ah, and a Muslim who fulfills this condition is called a mustati. The Hajj is a demonstration of the solidarity of the Muslim people, and their submission to God (Allah). The word Hajj means "to intend a journey", which connotes both the outward act of a journey and the inward act of intentions</p>
